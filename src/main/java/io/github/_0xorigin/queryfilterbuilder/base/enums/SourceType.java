package io.github._0xorigin.queryfilterbuilder.base.enums;

/**
 * Specifies the source of the filter or sort data.
 */
public enum SourceType {
    /**
     * The data is sourced from URL query parameters.
     */
    QUERY_PARAM,
    /**
     * The data is sourced from the HTTP request body.
     */
    REQUEST_BODY
}
